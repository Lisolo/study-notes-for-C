/*
we can use decimal or hexadecimal to indicate float constants.


10.0 -> 10
10.  -> 10
.123 -> 0.123


2.34E5 -> 2.34 * (10 ** 5)
67e-12 -> 67.0 * (10 ** -12)


The default values of float constants are double, 
but we can use suffix F to indicates float, 
suffix L to indicates long double type.
*/